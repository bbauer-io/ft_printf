#include "libft.h"

void	ft_putnbr_llong(long long n)
{
	if (n == -9223372036854775807)
	{
		ft_putstr("-9223372036854775807");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_llong(n / 10);
		ft_putnbr_llong(n % 10);
	}
	else
		ft_putchar(n + 48);
	return ;
}
